#include <bits/stdc++.h>
using namespace std;

int main() {
	int t, x, y;
	cin >> t;
	while(t--) {
	    cin >> x >> y;
	    double half = x / 2.0;
	    double(y) >= half ? cout << "YES" << endl : cout << "NO" << endl;
	}
	return 0;
}